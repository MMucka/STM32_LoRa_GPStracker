/**
  * @brief GPIO Initialization Function
  * @param None
  * @retval None
  */
static void MX_GPIO_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStruct = {0};

	/* GPIO Ports Clock Enable */
	__HAL_RCC_GPIOC_CLK_ENABLE();
	__HAL_RCC_GPIOH_CLK_ENABLE();
	__HAL_RCC_GPIOA_CLK_ENABLE();
	__HAL_RCC_GPIOB_CLK_ENABLE();

	/*Configure GPIO pin Output Level */
	HAL_GPIO_WritePin(GPIOA, GPS_ENABLE_Pin|LCD_ENABLE_Pin, GPIO_PIN_RESET);

#ifdef HF_FREQUENCY
	HAL_GPIO_WritePin(HF_GPIO_Port, HF_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(LF_GPIO_Port, LF_Pin, GPIO_PIN_RESET);
#else
	HAL_GPIO_WritePin(HF_GPIO_Port, HF_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(LF_GPIO_Port, LF_Pin, GPIO_PIN_SET);
#endif

#ifdef GPS_ENABLE
	HAL_GPIO_WritePin(GPS_ENABLE_GPIO_Port, GPS_ENABLE_Pin, GPIO_PIN_SET);
#endif

#ifdef LCD_ENABLE
	HAL_GPIO_WritePin(LCD_ENABLE_GPIO_Port, LCD_ENABLE_Pin, GPIO_PIN_SET);
#endif

	/*Configure GPIO pins : LCD_ENABLE_Pin HF_Pin LF_Pin GPS_ENABLE_Pin */
	GPIO_InitStruct.Pin = LCD_ENABLE_Pin|HF_Pin|LF_Pin|GPS_ENABLE_Pin|NSS_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
	HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

#ifdef ACC_INTERRUPT
//	Configure GPIO pins : ACC_INTERRUPT_Pin NSS_Pin GPS_INTERRUPT_Pin
	GPIO_InitStruct.Pin = ACC_INTERRUPT_Pin|GPS_INTERRUPT_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_IT_RISING;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);
#endif


#ifdef LORA_INTERRUPT
	/*Configure GPIO pins : DIO2_Pin DIO3_Pin DIO1_Pin DIO0_Pin */
	GPIO_InitStruct.Pin = DIO2_Pin|DIO3_Pin|DIO1_Pin|DIO0_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_IT_RISING;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);
#endif

	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);

	GPIO_InitStruct.Pin = LED_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
	HAL_GPIO_Init(LED_GPIO_Port, &GPIO_InitStruct);

	/* EXTI interrupt init*/
	HAL_NVIC_SetPriority(EXTI0_1_IRQn, 0, 0);
	HAL_NVIC_EnableIRQ(EXTI0_1_IRQn);

	HAL_NVIC_SetPriority(EXTI2_3_IRQn, 0, 0);
	HAL_NVIC_EnableIRQ(EXTI2_3_IRQn);

	HAL_NVIC_SetPriority(EXTI4_15_IRQn, 0, 0);
	HAL_NVIC_EnableIRQ(EXTI4_15_IRQn);
}


void system_init(){
	  HAL_Init();

	  SystemClock_Config();

	  MX_GPIO_Init();
	  MX_I2C1_Init();
	  MX_SPI1_Init();
	  MX_USART1_UART_Init();

	  uint8_t buffer[] = "STARTING\r\n";
	  HAL_UART_Transmit(&huart1, buffer, sizeof(buffer), HAL_MAX_DELAY);
}

